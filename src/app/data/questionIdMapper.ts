// Reverse Mapping für Lookup
const reverseMapping: { [newId: number]: number } = {};
Object.entries(idMapping).forEach(([oldId, newId]) => {
  reverseMapping[newId] = parseInt(oldId);
});

// Exportiere reorganisierte Fragen
export const questions: Question[] = originalQuestions
  .map(q => {
    const newId = idMapping[q.id];
    if (!newId) {
      console.warn(`Warning: Question ID ${q.id} has no mapping, keeping original ID`);
      return q;
    }
    return { ...q, id: newId };
  })
  .sort((a, b) => a.id - b.id); // Sortiere nach neuer ID

export type { Question };

